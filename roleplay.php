            <div>
                <h1>Role play : régles </h1>
                <br>
                <p><strong>Qu'est ce que le role play?:</strong><br>
                </p>
                <p align="left">Terme anglais signifiant littéralement "Jouer un rôle". Le "Roleplay"
                    désigne le fait d'incarner un personnage et d'agir comme le personnage (et non le joueur) le ferait.
                    Le
                    "roleplay" peut prendre des formes diverses (depuis le fait de s'exprimer en vieux français dans un
                    <a href="http://www.jeuxonline.info/lexique/mot/MMORPG">MMORPG</a> médiéval fantastique, jusqu'au
                    fait
                    de respecter
                    les principes de la <a href="http://www.jeuxonline.info/lexique/mot/Classe_personnage">classe du
                        personnage</a>).<br>
                </p>
                <p><strong>Règles de base :</strong></p>
                <p>Vous avez créé votre personnage; choisi son alignement de départ ? Bien, il est temps d'entrer sur la
                    scène et de participer à l'univers d'Althéa.<br>
                </p>
                <p>Il y'a diverses règles de base, extrêmement simples et très faciles à respecter avec un minimum de
                    bonne
                    volonté.<br>
                </p>
                <p>Tout d'abord, l'expression. Votre personnage a un esprit propre, et a une facon de parler propre à
                    lui
                    même
                    également. Faites le donc s'exprimer selon son humeur et sa mentalité. Notez également que
                    l'expression
                    "type moyenâgeuses" est de rigueur.
                    Le langage courant est à proscrire bien entendu.<br>
                </p>
                <p><strong>A - Le bonjour :</strong><br>
                </p>
                <p><strong>(A un homme) :</strong> Salutations Messire XXX *s'incline*</p>
                <p><strong>(A une femme) :</strong> Bonjour à vous Dame XXX *révérence*</p>
                <p><strong>(Général) :</strong> Salut à vous gueux ! *crache au sol*<br>
                </p>
                <p>Il existe des façons innombrables de dire bonjour. Encore une fois la mentalité de votre personnage
                    entre
                    en action. Les deux premiers exemples sont clairement pour des personnes de bon alignement, quant au
                    troisième il
                    serait plutôt réservé à des personnes mauvaises. Mais vous pouvez inventer diverses déclinaisons,
                    voire
                    même une
                    infinité de salutations.><br>
                </p>
                <p><strong>B - Prendre soin de l'autre:</strong><br>
                </p>
                <p>Comment vous portez vous en ce jour ?</p>
                <p>Vous etes vous remis de votre maladie ?</p>
                <p>Oh quel bien joli sourire, très cher, vous m'avez l'air en pleine forme !<br>
                </p>
                <p>Il existe tout comme les salutations diverses et nombreuses phrases de "prendre soin de l'autre",
                    tout comme chez nous il y'en a des dizaines également. Laissez votre imagination travailler.<br>
                </p>
                <p><strong>C - Quitter une conversation:</strong><br>
                </p>
                <p>A mon grand regret, je dois vous laisser.</p>
                <p>Pardonnez moi, mais le devoir m'appelle.</p>
                <p><strong>(A une femme) :</strong> Je dois partir maintenant. Au plaisir ! *baise main*</p>
                <p><strong>(A un ennemi) :</strong> A la prochaine... Dans votre tombe ! *se retire*</p>
                <br>
                <p>Quitter une conversation est généralement assez simple, et il n'y a pas besoin d'élucubrations de la
                    part
                    de votre personnage pour cela.<br>
                </p>
                <p><strong>- L'écriture :</strong></p>
                <p>Essayez surtout d'éviter les anachronismes. Vous ne possédez pas de télévision, de radio, de baladeur
                    CD.<br>
                </p>
                <p>Mettez une majuscule au début de chaque phrase que vous tapez. Si vous n'avez pas la place de
                    terminer
                    votre phrase, terminez la par des points de suspension (...) pour montrer à votre interlocuteur que
                    vous n'avez pas
                    fini, et ainsi qu'il ne réponde pas immédiatement. Continuez la phrase ensuite par ces mêmes points
                    de
                    suspension,
                    sans majuscule.<br>
                </p>
                <p>Evitez certaines incohérences, comme faire une révérence à une personne qui se trouve de l'autre coté
                    d'un
                    mur. Les gens dans la rue pourraient trouver ca étrange...<br>
                </p>
                <p>Utilisez des termes d'actions pour montrer les gestes de votre personnage. Les termes d'action,
                    cékoidon
                    ?
                    Ils sont placés entre deux astérisques, comme nous avons déjà vu précédemment : *révérence*, *larme
                    à
                    l'oeil*,
                    *sourit*, etc... Cela donne plus de vie à votre personnage que de simples paroles déblatérées sans
                    expression dans
                    la voix ni sur le visage. Le fameux *chuchotte* est également très utilisé.<br>
                </p>
                <p>Utilisez diverses mises en ligne selon ce que vous faites :</p>
                <p><strong>(blabla) :</strong> Pour le Hors RolePlay (désigné le plus souvent par HRP).</p>
                <p><strong>*blabla* :</strong> Désigne une émotion ou une action.</p>
                <p><strong>{blabla} :</strong> Permet d'écrire en gris, souvent utilisé pour montrer que le personnage
                    parle d'une voix sombre,
                    souvent mauvaise et inquiétante.</p>
                <p><strong>"blabla" :</strong> Permet d'écrire en blanc, notamment pour mettre certains mots en avant et
                    souligner ce qui est important.<br>
                </p>
                <p><strong>- Le respect de l'autre :</strong></p>
                <p>Soyez toujours cohérent et à d'affût de ce qui se passe. Mais n'oubliez pas une chose : derrière
                    chaque
                    personnage que vous rencontrez se trouve un joueur ou une joueuse. Votre personnage peut ne pas
                    respecter le moins
                    du monde la personne qu'il a en face de lui, le joueur (vous) a l'obligation de rester fair-play et
                    de
                    respecter le
                    joueur opposé, qui peut très bien être un ami dans la réalité.<br>
                </p>
                <p>Ainsi, n'agissez jamais de façon à ne laisser aucune possibilité de sortie à la personne qui vous
                    fait
                    face, sauf si la situation le permet et est grandement à votre avantage (cas extrême). Un jeu de
                    rôle
                    n'est pas fait
                    pour jouer "contre"; les autres, mais "avec" les autres.<br>
                </p>
                <p><strong>Conclusion :</strong></p>
                <p>Respect, cohérence, intégration, partage. Voila quelles sont les regles à respecter en toutes
                    situation.
                </p>



