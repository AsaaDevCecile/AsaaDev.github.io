<h2> Définition</h2>
<dl id="definitions">
    <dt>HTML</dt>
    <dd>Hyper Text Markup Language</dd>
    <dt>Adresse IP</dt>
    <dd>L'adresse IP ( Internet Protocol ) est un numéro d'identification qui est attribué à une machine
        connectée à Internet.</dd>

    <dt>Base de données</dt>
    <dd>Une base de données ( ou database en anglais ) est un conteneur qui permet de stocker des
        informations structurées et qui sont exploitables facilement.</dd>
    <dt>CRUD</dt>
    <dd>CRUD ( Create, Read, Update, Delete ) désigne les 4 opérations de base d'un logiciel. Le terme est
        un clin d'oeil au mot anglais crude qui signifie "rudimentaire".</dd>

    <dt>DOM</dt>
    <dd>Le Document Object Model ( ou DOM ) est un standard du W3C qui permet de créer une arborescence de
        la strcuture d'un document et de ses éléments.</dd>

    <dt>FTP</dt>
    <dd>FTP est l'acronyme de File Transport Protocol, il est un protocole de communication qui permet
        d'échanger des fichiers entre deux machines distantes.</dd>

    <dt>JSON</dt>
    <dd>JSON JavaScript Object Notation est un format de données qui permet de représenter une information
        structurée.</dd>

    <dt>Multi-thread</dt>
    <dd>Un logiciel multi-thread est capable d'exécuter plusieurs processus en même temps.</dd>

    <dt>Serveur web</dt>
    <dd>Le serveur web est une machine connectée à internet et qui heberge des ressources ( page web,
        images, vidéo, fichiers divers... )</dd>

    <dt>Thread</dt>
    <dd>Thread ( ou fil d'execution / tâche / processus ) est une execution d'une suite d'instructions.</dd>

    <dt>URL</dt>
    <dd>L'URL ( Uniform Resource Locator ) est une adresse web. Elle peut être composée de lettres et de
        chiffres.</dd>

    <dt>Virtual Host</dt>
    <dd>Virtual Host ( ou hébergement virtuel ) permet d'héberger plusieurs noms de domaine sur un seul et
        même serveur.
    </dd>


</dl>